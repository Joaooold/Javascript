let numero1 = 10
let numero2 = 15
// Adiçao

console.log(numero1 + numero2)

// Subtração

console.log(numero1 - numero2)

// Multiplicação

console.log(numero1 * numero2)

// Divisão

console.log(numero2 / numero1)

// Módulo - resto da divisão

let divisao = 9 / 5
let modulo = 9 % 5
console.log(divisao)
console.log(modulo) 

// Exponêciação - 2 elevado a 5 = 32

let numero = 2
let expoente = 5
let resultado = numero ** expoente
console.log(resultado)

// Incremento ++ (Adiciona mais 1) ou decremeto -- (Diminui em 1) 

let controle = 0
console.log(++controle)
console.log(++controle)
console.log(--controle)
