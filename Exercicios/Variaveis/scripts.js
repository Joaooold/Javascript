// TIPO LET
// tipo da variável + nome da variavel = informação da variável
let mensagen = 'Olá, Mundo';
console.log(mensagen);

mensagen = 'Oi, João';
console.log(mensagen);

let cor = 'vermelho';
console.log(cor);

cor = 'Azul'; // Variáveis do tipo let podem ser mudadas de informação
console.log(cor);

cor = 'Verde'; // Elas não podem ser repetidas let cor = "Azul"
console.log(cor);

cor = 'Preto';
console.log(cor);

cor = 'Rosa';
console.log(cor);

